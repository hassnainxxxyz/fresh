    {
        $first = Product::where('name', $query)->paginate(4);



        if(count($first) > 0) {

            return new ProductCollection($first);

        }
        $c = Category::where('name', $query)->first();
        if($c) {

            return new ProductCollection($c->products()->paginate(10));

        }
    }
